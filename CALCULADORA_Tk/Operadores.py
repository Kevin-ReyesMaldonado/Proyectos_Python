
def suma(n1, n2):
    result = n1 + n2
    return result

def subtraction(n1, n2):
    result = n1 - n2
    return result

def division(n1, n2):
    result = n1 / n2
    return result

def multiplication(n1, n2):
    result = n1 * n2
    return result

def potencia(n1, n2):
    result = n1 ** n2
    return result

def porciento(n1, n2):
    result = (n1 / 100) * n2
    return result
